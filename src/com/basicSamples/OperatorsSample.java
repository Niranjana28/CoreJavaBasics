package com.basicSamples;

public class OperatorsSample {

	public static void main(String[] args) {
		//declare variables
		int a=12, b=5;
		
		//addition operator
		System.out.println("a+b = "+(a+b));
		
		//subtraction operator
		System.out.println("a-b = "+(a-b));
		
		//multiplication operator
		System.out.println("a*b = "+(a*b));
		
		//division operator
		System.out.println("a/b = "+(a/b));
		
		//modulo operator
		System.out.println("a%b = "+(a%b));
		
		
		//create variables
		int ac=4;
		int var;
		
		//assign value using =
		var =ac;
		System.out.println("var using =: "+var);
		
		//assign value using =+
		var +=ac;
		System.out.println("var using +=: "+var);
		
		//assign value using =*
		var *=ac;
		System.out.println("var using *=: "+var);
				
		
	}

}
